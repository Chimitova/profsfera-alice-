function getAliceResponse(text) {
  const t = text.toLowerCase();

  // Точное совпадение по названию профессии
  const exactMatch = PROF_DB.find(p => t.includes(p.title.toLowerCase()));
  if (exactMatch) {
    return `${exactMatch.title} — отличный выбор! ${exactMatch.about} Зарплата: ${exactMatch.salary} рублей в месяц, спрос ${exactMatch.demand}. ${exactMatch.start} Хочешь узнать о другой профессии?`;
  }

  // Поиск по ключевым словам
  const kwMatch = PROF_DB.find(p => p.keywords.some(kw => t.includes(kw)));
  if (kwMatch) {
    return `По твоему запросу подходит профессия "${kwMatch.title}" в сфере ${kwMatch.cat}. ${kwMatch.about} Зарплата: от ${kwMatch.salary} рублей. ${kwMatch.start} Хочешь подробнее?`;
  }

  // Тематические ответы
  if (/медицин|врач|лечение|здоровье/.test(t))
    return 'В медицине сейчас самые перспективные: кибер-медик, нейродоктор и специалист по долголетию. Зарплаты на сорок-шестьдесят процентов выше традиционной практики. О какой из них рассказать подробнее?';
  if (/программирование|код|разработка|IT|айти/.test(t))
    return 'В IT самый высокий спрос на архитектора ИИ с зарплатой до пятисот тысяч, кибербезопасника до пятисот пятидесяти, и квантового разработчика до семисот тысяч. Что ближе — машинное обучение, безопасность или квантовые вычисления?';
  if (/творчество|дизайн|музыка|искусство/.test(t))
    return 'Творческие профессии с технологиями — золотое сочетание! XR-дизайнер, архитектор метавселенных, ИИ-музыкальный продюсер. Зарплаты от ста двадцати до четырёхсот тысяч. О чём рассказать?';
  if (/право|юрист|адвокат|закон/.test(t))
    return 'В праве три направления будущего: цифровой адвокат, этик ИИ и офицер по защите данных. Все активно нанимают, особенно в крупных ИТ-компаниях. Подробнее о каком?';
  if (/климат|экология|зелёная/.test(t))
    return 'Климатический сектор растёт сорок процентов в год. Инженер климатических решений зарабатывает от двухсот до четырёхсот двадцати тысяч. Это одна из самых быстрорастущих сфер на планете.';
  if (/зарплата|заработок|деньги|сколько/.test(t))
    return 'Топ три зарплаты: квантовый разработчик до семисот тысяч, робохирург до шестисот тысяч, нейроинтерфейс-инженер до шестисот тысяч. Всё зависит от навыков и региона. О какой профессии рассказать?';
  if (/с чего начать|как стать|как войти|учиться/.test(t))
    return 'Универсальный старт: первое — определи сферу интересов. Второе — пройди короткий вводный курс на Coursera или edX, многие бесплатны. Третье — сделай один практический проект. Четвёртое — найди ментора. Какую сферу хочешь исследовать?';
  if (/список|все профессии|какие профессии/.test(t))
    return 'Я знаю тридцать профессий по семи сферам: IT и данные, медицина, образование, право, климат, медиа, финансы и космос. Скажи сферу или название — расскажу подробно!';
  if (/спасибо|пока|выход|хватит|стоп/.test(t))
    return 'Было приятно помочь! Удачи в выборе профессии будущего. Возвращайся в ПрофСферу в любое время!';
  if (/привет|здравствуй|добро|начать/.test(t))
    return 'Привет! Я ИИ-консультант ПрофСферы. Знаю тридцать профессий будущего. Расскажи, какая сфера тебя интересует, или назови профессию — и я дам подробную информацию с зарплатой и планом старта.';

  // Fallback
  const r = PROF_DB[Math.floor(Math.random() * PROF_DB.length)];
  return `Расскажи подробнее о своих интересах — дам точную рекомендацию. Пока могу предложить профессию "${r.title}" в сфере ${r.cat}, зарплата от ${r.salary} рублей, спрос ${r.demand}. Хочешь узнать больше?`;
}

